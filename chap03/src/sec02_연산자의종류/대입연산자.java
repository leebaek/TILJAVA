package sec02_연산자의종류;

public class 대입연산자 {

	public static void main(String[] args) {
		// 대입 연산자
		int result = 0;
		result += 10;
		System.out.println("result=" + result);
		result -= 5;
		System.out.println("result=" + result);
		result *= 3;
		System.out.println("result=" + result);
		result /= 5;
		System.out.println("result=" + result);
		result %= 3;
		System.out.println("result=" + result);

	}

}
