package sec02.exam06;

public class EscapeExample {

	public static void main(String[] args) {
		System.out.println("번호\t이름\t직업"); // \t : 탭 만큼 띄어쓰기
		System.out.println("행 단위 출력\n"); // 엔터
		System.out.println("우리는 \"개발자\" 입니다."); // \" : 큰 따옴표 출력하는 법
		System.out.println("봄\\여름\\가을\\겨울"); // \\ : 역슬래시 출력하는 법
	}

}
